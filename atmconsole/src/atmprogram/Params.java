package atmprogram;

public class Params {
	private String parCmd;
	private String parSrc;
	private String parTgt;
	private int parNom;
	
	public String getParCmd() {
		return parCmd;
	}
	public void setParCmd(String parCmd) {
		this.parCmd = parCmd;
	}
	public String getParSrc() {
		return parSrc;
	}
	public void setParSrc(String parSrc) {
		this.parSrc = parSrc;
	}
	public String getParTgt() {
		return parTgt;
	}
	public void setParTgt(String parTgt) {
		this.parTgt = parTgt;
	}
	public int getParNom() {
		return parNom;
	}
	public void setParNom(int parNom) {
		this.parNom = parNom;
	}
	
	
}
